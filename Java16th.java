// Assignment operators
// =,+=,-=,*=,/=
public class Java16th
{
    public static void main(String arg[])
    {
        int a=50;
        a+=10; 
        int b=13;
        b-=5;
        int c=44;
        c*=2;
        int d=40;
        d/=5;
        int e=65;
        e%=3;
        System.out.println(a); //50+10=60
        System.out.println(b); //13-5=8
        System.out.println(c); //44*2=88
        System.out.println(d); //40/5=8
        System.out.println(e); //65%3=2
    }
}