// Binary Operator +,-,*,/,% 
public class Binary_Operator
{
    public static void main(String arg[])
    {
        int A=10;
        int B=5;
        System.out.println(" Addition of Two operant :" + (A+B));
        System.out.println(" Subtraction of Two operant :" + (A-B));
        System.out.println(" Multiplication of Two operant :" + (A*B));
        System.out.println(" Division of Two operant :" + (A/B));
        System.out.println(" Modulo of Two operant :" + (A%B));
    }
}