// Relational Operator ==,<,>,<=,>=,!=
public class Java14th
{
    public static void main(String arg[])
    {
        int x=12;
        int y=21;
        System.out.println(x==y); // 12==21 false
        System.out.println(x>y); //12>21 false
        System.out.println(x<y); //12<21 true
        System.out.println(x<=y); //12<=21 true
        System.out.println(x>=y); //12>=21 false
        System.out.println(x!=y); //12!=21 true
    }
}