// For loop using
import java.util.*;
public class Java40th {
    public static void main(String arg[])
    {
        Scanner sn=new Scanner(System.in);
        System.out.println("Enter the n number : ");
        int num=sn.nextInt();
        for( int i=1;i<=num;i++)
        {
            System.out.println("Hello world");
        }
    }
}
